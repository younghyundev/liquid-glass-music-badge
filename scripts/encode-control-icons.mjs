#!/usr/bin/env node
// Encode the PNG control icons in assets/controls/ as base64 data URIs and
// write the result to src/control-icons.ts. Run again whenever the source PNGs
// change:
//
//   node scripts/encode-control-icons.mjs

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const ASSETS_DIR = join(ROOT, "assets", "controls");
const OUTPUT = join(ROOT, "src", "control-icons.ts");

const NAMES = ["shuffle", "previous", "pause", "next", "repeat"];

const entries = NAMES.map((name) => {
  const data = readFileSync(join(ASSETS_DIR, `${name}.png`));
  return `  ${name}: "data:image/png;base64,${data.toString("base64")}"`;
});

const banner = `// Generated by scripts/encode-control-icons.mjs — DO NOT EDIT BY HAND.\n// Source PNGs live in assets/controls/. Re-run the script after replacing them.\n`;

const body = `import type { ControlName } from "./player-style";\n\nexport const CONTROL_ICON_DATA_URIS: Record<ControlName, string> = {\n${entries.join(",\n")}\n};\n`;

writeFileSync(OUTPUT, `${banner}\n${body}`);
console.log(`Wrote ${OUTPUT}`);
